def main() -> None:
    root = Path(__file__).resolve().parents[1]
    out_dir = root / "config" / "agent_providers"
    out_dir.mkdir(parents=True, exist_ok=True)
    tpl = (
        "id: hf_{stem}\n"
        "type: huggingface\n"
        "min_vram_gb: 0\n"
        "planner_hint: {hint}\n"
        "role: {role}\n"
        "goal: {goal}\n"
        "backstory: {backstory}\n"
        "model: {model}\n"
        "verbose: true\n"
        "allow_delegation: false\n"
    )
    for stem, model, hint, role, goal, backstory in ROWS:
        path = out_dir / f"hf_{stem}.yaml"
        path.write_text(
            tpl.format(
                stem=stem,
                hint=hint,
                role=role,
                goal=goal,
                backstory=backstory,
                model=model,
            ),
            encoding="utf-8",
        )
    print(f"Wrote {len(ROWS)} files to {out_dir}")


if __name__ == "__main__":
    main()
